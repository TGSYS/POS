
public class SKUItems {
private String code;
private Integer price;
public SKUItems(String code, Integer price) {
	super();
	this.code = code;
	this.price = price;
}
public String getCode() {
	return code;
}
public void setCode(String code) {
	this.code = code;
}
public Integer getPrice() {
	return price;
}
public void setPrice(Integer price) {
	this.price = price;
}

}
